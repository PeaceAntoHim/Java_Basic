public class OperationRatio {

    public static void main(String[] args) {
        int value1 = 100;
        int value2 = 200;

        System.out.println(value1 == value2);
        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 != value2);

    }

}
